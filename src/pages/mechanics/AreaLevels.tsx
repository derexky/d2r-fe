function levelColor(v: string | number) {
  if (v === '－') return '#333';
  const n = Number(v);
  if (n >= 85) return '#ff4444';
  if (n >= 80) return '#ff8844';
  if (n >= 75) return '#ffcc44';
  if (n >= 60) return '#88cc44';
  return '#aaaaaa';
}

